void main(){

  // != (Tidak sama dengan)
  int  num1 = 10;
  int num2 = 9;

  if(num1 != num2){
    print('true');
  }else {
    print('false');
  }

  // < (Lebih kecil)
  if(num1 < num2){
    print('true');
  }else {
    print('false');
  }

  // > (Lebih besar)
  if(num1 > num2){
    print('true');
  }else {
    print('false');
  }

  // == (Sama dengan)
  if(num1 == num2){
    print('true');
  }else {
    print('false');
  }

  // >= (Lebih besar sama dengan)
  if(num1 >= num2){
    print('true');
  }else {
    print('false');
  }

  // <= (Lebih besar sama dengan)
  if(num1 <= num2){
    print('true');
  }else {
    print('false');
  }

}